grade = int(input("Enter your grade: "))
if grade >= 95 and grade <= 100:
    print("Excellent")
if grade >= 85 and grade <= 94:
    print("Very good")
if grade >= 75 and grade <= 84:
    print("Good")
if grade >= 65 and grade <= 74:
    print("Satisfactory")
if grade >= 60 and grade <= 64:
    print("Marginal")
if grade >= 0 and grade <= 59:
    print("Unsatisfactory")
